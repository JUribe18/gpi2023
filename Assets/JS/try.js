/* SLIDE PRINCIPAL */
let slideIndex = 1;
showSlides(slideIndex);
function plusSlides(n) {
  showSlides(slideIndex += n);
}
function showSlides(n) {
  let i;
  const slides = document.getElementsByClassName("sld-fade");
  if (n > slides.length) { slideIndex = 1 }
  if (n < 1) { slideIndex = slides.length }
  for (i = 0; i < slides.length; i++) {
    slides[i].style.display = "none";
  }
  slides[slideIndex - 1].style.display = "block";
}

/* SLIDE PRODCARD */
let indiceslide = 1;
mostrarslide(indiceslide);
function siguiente(x){
  mostrarslide(indiceslide +=x);
}
function mostrarslide(x){
  let z;
  const sld = document.getElementsByClassName("sld-fade-mod");
  if (x > sld.length) { indiceslide = 1 }
  if (x < 1) { indiceslide = sld.length }
  for (z = 0; z < sld.length; z++){
    sld[z].style.display = "none";
  }
  sld[indiceslide - 1].style.display = "block";
}


// Función para abrir el modal con detalles del producto
function openModal(productData) {
  const modal = document.getElementById("detalles-pop");
  const productDetails = document.getElementById("prodet");
  const sldcont = document.createElement("div");
  sldcont.className = "sld-cont";
  
  //Limpia el contenido anterior
  productDetails.innerHTML = "";
  indiceslide = 1;
  //carga dinámicamente las imágenes y detalles del producto según el productData
  productDetails.innerHTML += `<h3>${productData.name}</h3>`;
  productData.images.forEach(image => {
    const slide = document.createElement("div");
    slide.className = "sld-fade-mod";
    slide.innerHTML = `<img src="${image}" style="width:100%" id="imagenmodal" onmouseover="zoomIn(this)" onmouseout="zoomOut(this)">`;
    sldcont.appendChild(slide);
  });
  productDetails.appendChild(sldcont);
  productDetails.innerHTML += `<p>${productData.description}</p>`;
  productDetails.innerHTML += `<a class="prev" onclick="siguiente(-1)">&#10094;</a>`;
  productDetails.innerHTML += `<a class="next" onclick="siguiente(1)">&#10095;</a>`;
  
  modal.style.display = "block";
  mostrarslide(indiceslide);
}
// Función para cerrar el modal
function closeModal() {
  const modal = document.getElementById("detalles-pop");
  modal.style.display = "none";
}

function scrollToDiv(id) {
  var element = document.getElementById(id);
  element.scrollIntoView({ behavior: 'smooth' });
}

function zoomIn(element) {
  element.classList.add('zoomed');
}
function zoomOut(element) {
  element.classList.remove('zoomed');
}

